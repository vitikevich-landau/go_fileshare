package client

import "time"

// ─────────────────────────────────────────────────────────────────────────────
// types.go — словарь предметной области пакета client.
//
// client — это БЛОКИРУЮЩИЙ транспорт протокола fileshare v2: им пользуются и TUI
// (fshare-commander), и режим --batch. Он проводит рукопожатие, шлёт запросы,
// качает файлы с докачкой и ПРОЗРАЧНО переправляет асинхронные кадры
// EVENT_*/PONG в обработчик событий, пока ждёт конкретный ответ
// (docs/tz/09-go-port.md §5.8).
//
// Ключевая тонкость «блокирующего» клиента: запросные методы (List, Stat,
// Download, …) нельзя вызывать конкурентно друг с другом — они по очереди пишут
// запрос и читают ответ из одного сокета. А вот Ping/Interrupt/Close можно
// звать из другой горутины (для heartbeat и отмены). В TUI это разделение
// обеспечивает мьютекс вокруг клиента (см. tui.Model.clientMu).
// ─────────────────────────────────────────────────────────────────────────────

// ServerAddr — адрес сервера в форме «host:port», как его передают в Dial.
// Псевдоним string.
type ServerAddr = string

// ClientName — самоназвание клиента, отправляемое в HELLO (напр.
// «fshare-commander»). Псевдоним string.
type ClientName = string

// Login — учётная запись для входа. Псевдоним string.
type Login = string

// Password — пароль в открытом виде; используется только чтобы вычислить
// доказательство (см. пакет auth) и по сети не отправляется. Псевдоним string.
type Password = string

// Path — путь на сервере (в терминах VFS, от корня раздачи). Псевдоним string.
type Path = string

// LocalPath — путь на локальной файловой системе клиента (куда сохраняется
// скачиваемый файл, рядом с ним живёт «<имя>.part»). Псевдоним string.
type LocalPath = string

// SessionID — номер сессии, выданный сервером в AUTH_OK. Псевдоним uint64.
type SessionID = uint64

// Iterations — число итераций PBKDF2, объявленное сервером в HELLO_OK; клиент
// применяет его при выводе ключа из пароля (с верхним пределом maxAuthIters
// против враждебного сервера, CR-04). Псевдоним int.
type Iterations = int

// DialTimeout — предельное время на установку соединения и всё рукопожатие,
// чтобы молчащий или MITM-сервер не подвесил клиента навсегда (CR-04).
// Псевдоним time.Duration.
type DialTimeout = time.Duration
