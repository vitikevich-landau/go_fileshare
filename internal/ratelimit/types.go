package ratelimit

// ─────────────────────────────────────────────────────────────────────────────
// types.go — словарь предметной области пакета ratelimit.
//
// Пакет ограничивает скорость скачивания по схеме «token bucket» (ведро
// токенов), причём НА ДВУХ уровнях сразу:
//
//   • глобальный лимит — суммарная отдача всего сервера;
//   • per-client лимит — на один КЛЮЧ КЛИЕНТА (логин), поэтому N параллельных
//     закачек одного пользователя делят одно ведро, а не получают по лимиту
//     каждая (§8 bug 11).
//
// Идея token bucket: ведро наполняется токенами со скоростью «лимит байт/с» и
// имеет глубину burst. Чтобы отдать n байт, из ведра забирают n токенов; если их
// не хватает — Wait блокируется ровно на столько, сколько нужно, чтобы они
// накопились. Лимиты читаются НА КАЖДОМ чанке, поэтому изменение конфига
// (см. пакет config) тормозит или ускоряет уже идущую передачу без разрыва
// соединения (docs/tz/09-go-port.md §5.6).
// ─────────────────────────────────────────────────────────────────────────────

// ClientKey — ключ, по которому считается персональное ведро. Это ЛОГИН
// пользователя (а не адрес сокета), чтобы несколько соединений одного человека
// делили общий бюджет скорости. Псевдоним string.
type ClientKey = string

// BytesPerSecond — лимит скорости в байтах в секунду; 0 означает «без
// ограничения». Псевдоним uint64.
type BytesPerSecond = uint64

// ByteCount — сколько байт просят пропустить за один вызов Wait (обычно размер
// чанка). Псевдоним int (тип, которого ждёт rate.Limiter.WaitN).
type ByteCount = int
