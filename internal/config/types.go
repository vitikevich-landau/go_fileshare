package config

// ─────────────────────────────────────────────────────────────────────────────
// types.go — словарь предметной области пакета config.
//
// Пакет решает одну задачу: менять настройки демона НА ЛЕТУ, без перезапуска и
// без блокировок на горячем пути (каждый чанк передачи, каждый accept читают
// настройки). Механизм — СНАПШОТЫ:
//
//   • Settings — плоская value-структура (без указателей/срезов/карт), поэтому
//     её снимок (snapshot) — это дешёвая копия ПО ЗНАЧЕНИЮ.
//   • Hub хранит указатель на текущий снапшот в atomic.Pointer. Читатели делают
//     ОДНУ атомарную загрузку (Current) и никогда не блокируются.
//   • Писатель (админ-команда / SIGHUP) строит НОВЫЙ снапшот, валидирует его и
//     атомарно ПОДМЕНЯЕТ указатель. Старый снапшот, который кто-то уже читает,
//     остаётся неизменным — гонок нет по построению.
//
// Так изменение (например, лимита скорости) применяется мгновенно ко всем
// активным передачам на следующем чанке (docs/tz/09-go-port.md §5.4, §12.1).
//
// ─── Горячие и рестарт-настройки ─────────────────────────────────────────────
//
// Не всё можно поменять на лету. У каждого поля в комментарии помечено:
//   • hot     — применяется немедленно через подмену снапшота;
//   • restart — вступает в силу только при перезапуске демона (порт, share_root,
//     число воркеров, параметры watcher-а и т.п.). Набор рестарт-ключей задан в
//     restartKeys (hub.go); Set отвергает попытку изменить их на лету.
// ─────────────────────────────────────────────────────────────────────────────

// Port — TCP-порт, на котором слушает демон. Псевдоним int (rest­art-настройка).
type Port = int

// BytesPerSecond — лимит скорости в байтах в секунду (0 — без ограничения).
// Псевдоним uint64.
type BytesPerSecond = uint64

// Seconds — длительность в секундах в конфиге (тайм-ауты рукопожатия/простоя,
// срок бана). Псевдоним int. При применении переводится в time.Duration.
type Seconds = int

// Milliseconds — длительность в миллисекундах в конфиге (debounce watcher-а).
// Псевдоним int.
type Milliseconds = int

// ConfigKey — «точечный» ключ настройки, напр. «limits.per_client_bps». Так его
// адресует админ в ADMIN_SET и так он показывается в админ-панели. Псевдоним string.
type ConfigKey = string

// ConfigValue — значение настройки в СТРОКОВОМ виде (как ходит по админ-каналу и
// показывается в панели); applyKey разбирает его по типу конкретного ключа.
// Псевдоним string.
type ConfigValue = string
