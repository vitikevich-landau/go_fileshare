package tui

// ─────────────────────────────────────────────────────────────────────────────
// types.go — словарь предметной области пакета tui.
//
// tui — полноэкранный клиент в стиле Midnight Commander (fshare-commander) на
// фреймворке Bubble Tea. Понимание пакета держится на двух идеях.
//
// ─── 1. Архитектура Bubble Tea: Model – Update – View ────────────────────────
//
// Bubble Tea реализует паттерн Elm: всё состояние UI живёт в ОДНОЙ структуре
// Model, а меняется только через чистую функцию Update(msg) → (Model, Cmd):
//
//	  ┌──────────────┐   сообщение (tea.Msg)    ┌───────────────┐
//	  │   события:    │ ───────────────────────▶ │    Update      │
//	  │ клавиши, тик, │                          │ меняет Model,  │
//	  │ ответы сети   │ ◀─────────────────────── │ возвращает Cmd │
//	  └──────────────┘   команда (tea.Cmd)       └──────┬────────┘
//	          ▲                                          │
//	          │                  View(Model) → строка    ▼
//	          └──────────────  перерисовка экрана  ──────┘
//
//   • Model (model.go) — ВСЁ состояние: экран, панели, курсоры, очередь передач,
//     лог операций, состояние админ-панели и реконнекта. Нет глобальных
//     переменных — это делает логику тестируемой без терминала.
//   • Update (model.go) — единственное место, где Model меняется, в ответ на
//     tea.Msg. Никаких мутаций из фоновых горутин напрямую.
//   • View (view.go) — чистая функция: из Model рисует кадр экрана, ничего не
//     меняя.
//   • tea.Cmd — способ запустить побочный эффект (сетевой запрос, таймер) и
//     вернуть его результат обратно в Update как новое сообщение.
//
// ─── 2. Сеть строго ВНЕ UI-потока ────────────────────────────────────────────
//
// Железное правило (docs/tz/01-architecture.md §4): сетевой ввод-вывод никогда
// не выполняется в UI-потоке — иначе любой лаг сети заморозил бы интерфейс.
// Поэтому:
//   • запросы уходят как tea.Cmd (выполняются в отдельной горутине и возвращают
//     результат сообщением);
//   • фоновый «насос» (conn.go: runPump) в своей горутине опрашивает события и
//     шлёт heartbeat, переправляя пришедшие кадры в канал events НЕблокирующе;
//   • доступ к client сериализуется мьютексом clientMu, потому что блокирующий
//     транспорт нельзя использовать из нескольких горутин одновременно;
//   • при обрыве — экспоненциальный реконнект (beginReconnect), не трогая UI.
//
// Словарь ниже даёт доменные имена «сырым» примитивам этих структур.
// ─────────────────────────────────────────────────────────────────────────────

// Host — хост сервера, к которому подключается клиент. Псевдоним string.
type Host = string

// Port — TCP-порт сервера. Псевдоним int.
type Port = int

// RemotePath — путь на сервере (в терминах VFS). Псевдоним string.
type RemotePath = string

// LocalPath — путь на локальной файловой системе клиента. Псевдоним string.
type LocalPath = string

// FileName — имя файла без пути (как показывается в панели). Псевдоним string.
type FileName = string

// ByteSize — размер в байтах (получено/всего в передаче, размер записи).
// Псевдоним uint64.
type ByteSize = uint64
