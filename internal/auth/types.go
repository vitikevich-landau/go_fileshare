package auth

import "github.com/vitikevich-landau/go_fileshare/internal/proto"

// ─────────────────────────────────────────────────────────────────────────────
// types.go — словарь предметной области пакета auth.
//
// Пакет реализует SCRAM-подобную аутентификацию challenge–response. Два её
// свойства и объясняют всю конструкцию:
//
//   1. Пароль НИКОГДА не пересекает сеть — ни открытым текстом, ни хешем.
//   2. Кража users.json (в нём лежит только StoredKey) НЕ позволяет войти:
//      из StoredKey нельзя восстановить ни пароль, ни доказательство.
//
// (docs/tz/09-go-port.md §5.3, docs/tz/06-security.md §3.)
//
// ─── Цепочка ключей SCRAM ──────────────────────────────────────────────────────
//
// Все промежуточные значения — 32 байта, но означают РАЗНОЕ, и путать их нельзя.
// Стрелка «→» читается «однонаправленно выводится из» (обратно не развернуть):
//
//	password
//	   │  PBKDF2-HMAC-SHA256(password, salt=saltFor(login), iters)
//	   ▼
//	SaltedPassword ──HMAC(·,"Client Key")──▶ ClientKey ──SHA256──▶ StoredKey
//	                                             │                     │
//	                              хранится ТОЛЬКО StoredKey ───────────┘
//	                                        (в users.json)
//
// Доказательство (ClientProof), которое клиент кладёт в AUTH_REQUEST:
//
//	ClientProof = ClientKey XOR HMAC(StoredKey, challenge || login)
//
// Проверка на сервере (Verify): по ClientProof и HMAC(StoredKey, challenge||login)
// сервер восстанавливает ClientKey, берёт SHA256 и сверяет с хранимым StoredKey —
// сравнение КОНСТАНТНОГО ВРЕМЕНИ, чтобы не «протекала» информация через тайминг.
//
// Почему кража users.json бесполезна: там только StoredKey. Чтобы подделать
// ClientProof, нужен ClientKey, а он выводится из пароля и в базе его нет.
// ─────────────────────────────────────────────────────────────────────────────

// Login — учётная запись пользователя. Псевдоним string.
type Login = string

// Password — пароль в открытом виде. Живёт лишь мгновение в памяти при выводе
// ключей и НИКОГДА не сохраняется и не отправляется по сети. Псевдоним string.
type Password = string

// Iterations — число итераций PBKDF2 при выводе SaltedPassword из пароля. Больше
// итераций — дороже перебор для атакующего. Порог по умолчанию — DefaultIters
// (600k). Псевдоним int (в отличие от proto.PBKDF2Iterations — там это u32 для
// провода; здесь это внутренний параметр Go-функций).
type Iterations = int

// ScramKey — 32-байтное промежуточное значение цепочки SCRAM (SaltedPassword,
// ClientKey, StoredKey — все имеют эту форму). Отдельные ИМЕНА этих значений
// (ClientKey/StoredKey/…) заняты функциями пакета, поэтому в сигнатурах для
// самого 32-байтного типа используется этот общий псевдоним; СМЫСЛ конкретного
// значения поясняет имя параметра и докстринг функции.
type ScramKey = [32]byte

// ClientProof — доказательство знания пароля, которое клиент помещает в
// AUTH_REQUEST. Совпадает по размеру с proto.Proof (ProofLen байт).
type ClientProof = [proto.ProofLen]byte

// ClientIP — адрес клиента, по которому Guard считает неудачные попытки входа и
// выдаёт бан. Псевдоним string.
type ClientIP = string

// FailCount — счётчик подряд идущих неудачных попыток аутентификации с одного IP.
// Псевдоним int.
type FailCount = int
