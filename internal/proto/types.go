package proto

import "time"

// ─────────────────────────────────────────────────────────────────────────────
// types.go — словарь предметной области пакета proto.
//
// Здесь нет исполняемой логики — только объявления типов. Цель файла в том,
// чтобы сигнатуры сообщений и функций читались как предложения о протоколе, а
// не как набор безымянных чисел: не «Offset uint64, TotalSize uint64», а
// «Offset ByteOffset, TotalSize FileSize». Прочитав объявление структуры, сразу
// понимаешь, ЧТО означает каждое поле на проводе, а не только сколько оно
// занимает байт.
//
// ВАЖНО про два вида объявлений типов в Go:
//
//	type Login    = string   // ← ПСЕВДОНИМ (есть знак «=»)
//	type Msg      uint8       // ← НОВЫЙ ТИП  (знака «=» нет)
//
//   • Псевдоним (alias, со знаком «=») — это буквально второе имя того же самого
//     типа. Login и string полностью взаимозаменяемы; у псевдонима нельзя
//     завести собственные методы. Все доменные имена в ЭТОМ файле —
//     псевдонимы: это делает их «прозрачными» для сериализатора (codec.go
//     работает с сырыми uint8/16/32/64), поэтому переименование поля НЕ меняет
//     ни один байт на проводе и не может сломать совместимость с эталонным
//     C++-клиентом.
//   • Новый (defined) тип, без «=» — это самостоятельный тип, которому уже
//     можно дать методы. Именно так объявлены ПЕРЕЧИСЛЕНИЯ протокола в proto.go
//     (Msg, Role, Kind, FsOp, Severity, ErrCode, AuthMode, Algo,
//     AuthFailReason). У части из них есть методы (Msg.Known/String,
//     Role.String, ErrCode.String), остальные — просто именованные наборы
//     констант; на проводе все они кодируются одним-двумя байтами. Их не стали
//     переносить сюда, чтобы держать перечисление рядом с его константами.
//
// Итого «карта» типов протокола:
//   • примитивы-величины (счётчики, размеры, идентификаторы) — псевдонимы ниже;
//   • перечисления с фиксированным набором значений — defined-типы в proto.go;
//   • сами сообщения (Hello, DownloadAccept, …) — структуры в messages.go.
// ─────────────────────────────────────────────────────────────────────────────

// ─── Идентификаторы ──────────────────────────────────────────────────────────

// ProtocolVersion — номер версии протокола, которым обмениваются в HELLO и
// HELLO_OK. Первым делом сервер и клиент сверяют её: сервер v2 корректно
// отказывает клиенту v1 и наоборот (единственная согласованная версия — 2,
// константа ProtoVersion). На проводе — u16, big-endian.
type ProtocolVersion = uint16

// SessionID — уникальный номер сессии, который сервер выдаёт клиенту в AUTH_OK.
// По нему админ адресует kick (ADMIN_KICK) и находит клиента в списке
// ADMIN_CLIENTS. Монотонно растёт, никогда не переиспользуется в рамках жизни
// демона. На проводе — u64.
type SessionID = uint64

// TransferID — номер одной операции скачивания в пределах сессии. Сервер
// присваивает его в DOWNLOAD_ACCEPT; все последующие CHUNK_DATA / DOWNLOAD_DONE
// и встречный DOWNLOAD_CANCEL несут этот же id, чтобы отменить/завершить именно
// текущую передачу, а не соседнюю (см. защиту от «поздней отмены», R3-2). На
// проводе — u32.
type TransferID = uint32

// ─── Величины в байтах ───────────────────────────────────────────────────────

// ByteOffset — смещение в байтах от начала файла. Клиент шлёт его в
// DOWNLOAD_REQUEST, чтобы ДОКАЧАТЬ уже частично скачанный файл (offset > 0, если
// на диске лежит «<имя>.part»). На проводе — u64.
type ByteOffset = uint64

// FileSize — размер файла в байтах (полный размер файла на сервере или общий
// объём передачи в DOWNLOAD_ACCEPT.TotalSize). На проводе — u64.
type FileSize = uint64

// ByteCount — накопительный счётчик переданных байт (сколько всего отдано
// клиенту / сервером). Отличается от FileSize смыслом: это «пройдено», а не
// «сколько всего». На проводе — u64.
type ByteCount = uint64

// BytesPerSecond — скорость или ограничение скорости в байтах в секунду.
// Используется и для лимитов (на клиента / глобальный), и для измеренной
// текущей скорости клиента в админ-статистике. 0 означает «без ограничения».
// На проводе — u64.
type BytesPerSecond = uint64

// ─── Время ───────────────────────────────────────────────────────────────────

// UnixSeconds — момент времени как число секунд от начала эпохи Unix. Так на
// проводе передаётся mtime файла (DirEntry.Mtime, EVENT_FS.Mtime). Отдельный тип
// от DurationSeconds, потому что это ТОЧКА во времени, а не длительность.
// На проводе — u64.
type UnixSeconds = uint64

// DurationSeconds — длительность в секундах (например, uptime сервера в
// ADMIN_STATS_RESPONSE). На проводе — u64.
type DurationSeconds = uint64

// GracePeriodSeconds — сколько секунд сервер даёт активным передачам доиграть
// перед принудительным закрытием при ADMIN_SHUTDOWN (graceful drain).
// На проводе — u32.
type GracePeriodSeconds = uint32

// ─── Аутентификация ──────────────────────────────────────────────────────────

// PBKDF2Iterations — число итераций PBKDF2 при выводе ключа из пароля. Сервер
// сообщает его клиенту в HELLO_OK, чтобы тот посчитал доказательство теми же
// параметрами. Ниже установленного порога (600k) конфиг отвергается при
// загрузке. На проводе — u32.
type PBKDF2Iterations = uint32

// Challenge — случайный «вызов» (nonce), который сервер присылает в HELLO_OK.
// Клиент подмешивает его в доказательство, поэтому пароль никогда не ходит по
// сети открытым текстом, а перехваченное доказательство нельзя переиграть на
// новый вызов (challenge–response). Длина фиксирована: ChallengeLen байт.
type Challenge = [ChallengeLen]byte

// Proof — доказательство знания пароля, которое клиент присылает в
// AUTH_REQUEST: HMAC от challenge на ключе, выведенном из пароля. Длина
// фиксирована: ProofLen байт.
type Proof = [ProofLen]byte

// ─── Файловая система и передачи ─────────────────────────────────────────────

// Path — путь внутри раздачи (VFS), всегда в «серверных» терминах: от корня
// share-root, разделитель «/». Именно Path фигурирует в LIST_DIR, STAT,
// CHECKSUM, DOWNLOAD_REQUEST и в событиях EVENT_FS. Максимальная длина —
// MaxPathLen. На проводе — строка с префиксом длины u16.
type Path = string

// FileName — имя одной записи каталога (без пути), например «report.pdf».
// Максимальная длина — MaxNameLen. На проводе — строка с префиксом длины u16.
type FileName = string

// EntryFlags — битовые флаги записи каталога (см. FlagNew: запись «новая» с
// момента прошлого визита клиента). На проводе — u8.
type EntryFlags = uint8

// EntryCount — число элементов в списке на проводе (записей каталога в
// LIST_DIR_RESPONSE, клиентов в ADMIN_CLIENTS). Ограничено MaxListEntries,
// чтобы злонамеренный счётчик не заставил получателя выделить гигабайты.
// На проводе — u32.
type EntryCount = uint32

// Checksum — контрольная сумма файла (алгоритм задаёт соседнее поле Algo).
// Сервер присылает её в CHECKSUM_RESPONSE и DOWNLOAD_DONE, клиент сверяет её со
// своей после докачки. Длина фиксирована: ChecksumLen байт (буфер рассчитан на
// SHA-256; CRC32 занимает первые 4 байта).
type Checksum = [ChecksumLen]byte

// ─── Счётчики и разное ───────────────────────────────────────────────────────

// Count — обобщённый счётчик «штук» для админ-статистики (сколько завершено
// передач, активных соединений, активных закачек, раздаваемых файлов).
// На проводе — u64.
type Count = uint64

// SubscriptionMask — битовая маска подписки на push-события, которую клиент
// шлёт в SUBSCRIBE. Складывается из битов SubFS / SubNotice / SubConfig. Сервер
// хранит её на сессии и рассылает событие только подписанным. На проводе — u32.
type SubscriptionMask = uint32

// Login — логин пользователя (учётная запись). Фигурирует в AUTH_REQUEST и в
// админ-списке клиентов. На проводе — строка с префиксом длины u16.
type Login = string

// ClientAddr — сетевой адрес клиента (IP или IP:port) в человекочитаемом виде.
// Сервер показывает его админу в ADMIN_CLIENTS. На проводе — строка с префиксом
// длины u16.
type ClientAddr = string

// ─── Тайминги протокола (не ходят по проводу) ────────────────────────────────
//
// Значения ниже — это КЛИЕНТ- и СЕРВЕР-локальные настройки времени, а не поля
// сообщений. Они собраны здесь как часть словаря, чтобы у «магических»
// длительностей было доменное имя. time.Duration выбран (а не секунды-числа),
// потому что это внутренние тайминги Go-кода, где Duration удобнее и безопаснее.

// HandshakeTimeout — сколько сервер ждёт завершения рукопожатия (HELLO→AUTH_OK),
// прежде чем разорвать полуоткрытое соединение. Защита от «висящих» подключений,
// которые заняли слот, но так и не аутентифицировались.
type HandshakeTimeout = time.Duration

// HeartbeatInterval — период, с которым сторона шлёт PING, чтобы подтвердить
// живость канала и вовремя заметить обрыв «тихого» TCP-соединения.
type HeartbeatInterval = time.Duration
