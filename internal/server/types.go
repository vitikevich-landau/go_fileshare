package server

// ─────────────────────────────────────────────────────────────────────────────
// types.go — словарь предметной области пакета server.
//
// Сервер — это демон fileshare v2: цикл приёма соединений, рукопожатие и цикл
// обработки запросов на каждое соединение, реестр сессий, обработчики файловой
// системы и передач, админ-канал и корректная остановка (docs/tz/09-go-port.md
// §5.5).
//
// ─── Модель конкурентности: горутина(ы) на соединение ────────────────────────
//
// На каждое принятое TCP-соединение сервер запускает НЕСКОЛЬКО горутин с чётким
// разделением ролей — так медленный клиент тормозит только себя, а не других:
//
//	handleConn (владелец соединения)
//	  ├─ writeLoop        — ЕДИНСТВЕННЫЙ писатель в сокет: тянет кадры из
//	  │                     канала out, поэтому кадры на проводе атомарны, а
//	  │                     очередь даёт backpressure только этому клиенту.
//	  ├─ serveRequests    — ЕДИНСТВЕННЫЙ читатель из сокета: разбирает кадры,
//	  │                     проверяет права и вызывает обработчик.
//	  ├─ idleWatchdog     — «сторож простоя»: закрывает соединение, если оно
//	  │                     молчит дольше idle_timeout_s (кроме активной передачи).
//	  └─ (на время закачки) streamFile — отдельная горутина отдаёт файл чанками,
//	                        чтобы читатель оставался свободен для PING/CANCEL.
//
// Ключевой приём: читатель НЕ пишет в сокет напрямую — всё исходящее идёт через
// канал out и один writeLoop. Рассылки (broadcast) используют НЕблокирующую
// отправку и отбрасываются, если очередь клиента переполнена, чтобы один
// тормозящий клиент не задержал доставку остальным (docs/tz/09-go-port.md §5.5).
//
// ─── Состояние сессии: HANDSHAKE → рабочее ───────────────────────────────────
//
// До успешного AUTH соединение в состоянии HANDSHAKE и принимает только
// HELLO/AUTH_*/PING; всё остальное — ошибка и разрыв. Тайм-аут на рукопожатие
// защищает от «висящих» полуоткрытых соединений. После AUTH_OK сессия знает,
// кто вошёл и с какой ролью, а диспетчер сверяет роль с минимально требуемой для
// каждого типа сообщения (см. MinRole) — единая точка авторизации.
// ─────────────────────────────────────────────────────────────────────────────

// SessionID — уникальный номер сессии, который сервер присваивает при подключении
// (монотонный счётчик nextSession). Псевдоним uint64.
type SessionID = uint64

// TransferID — номер одной операции скачивания в пределах сессии (счётчик
// nextTransfer). По нему сопоставляются CHUNK_DATA и встречный DOWNLOAD_CANCEL.
// Псевдоним uint32.
type TransferID = uint32

// ClientIP — адрес клиента (host без порта), используется для логов и для
// анти-brute-force гарда. Псевдоним string.
type ClientIP = string

// ByteCount — счётчик переданных байт (на сессию и суммарно по серверу).
// Псевдоним uint64.
type ByteCount = uint64

// GraceSeconds — сколько секунд дать активным передачам доиграть при остановке
// перед принудительным закрытием сокетов. Псевдоним uint32 (как в проводном
// ADMIN_SHUTDOWN).
type GraceSeconds = uint32
