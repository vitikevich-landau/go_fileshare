package vfs

// ─────────────────────────────────────────────────────────────────────────────
// types.go — словарь предметной области пакета vfs.
//
// VFS (virtual file system) раздаёт дерево каталогов, ЗАПЕРТОЕ внутри одной
// корневой директории (share-root). Всё удержание в границах корня делегировано
// os.Root (Go 1.24+): даже путь с «..» или симлинк наружу не выведет операцию за
// пределы корня. Это заменяет ручную логику openat2/realpath из эталона на C++ и
// закрывает класс уязвимостей TOCTOU по построению (docs/tz/09-go-port.md §5.2,
// §7).
//
// ─── Три формы пути ───────────────────────────────────────────────────────────
//
// Главная терминологическая тонкость пакета — путь существует в ТРЁХ видах, и их
// нельзя путать:
//
//	VirtualPath   "/../a//b/"  ← как прислал клиент: возможны «..», «//», слэши
//	   │  CleanPath()
//	   ▼
//	cleaned       "/a/b"       ← нормализован от корня «/»: без «..», «//», хвоста
//	   │  rel()
//	   ▼
//	OS-relative   "a/b"        ← путь для os.Root: без ведущего «/», в разделителях ОС
//
// Наружу (в протокол и клиенту) всегда идёт cleaned-форма — единый «серверный»
// путь с разделителем «/». Внутрь os.Root подаётся только OS-relative-форма.
// «..» никогда не поднимается выше корня, потому что путь чистится так, будто он
// укоренён в «/».
// ─────────────────────────────────────────────────────────────────────────────

// VirtualPath — путь в том виде, в каком его прислал клиент, ДО нормализации.
// Может содержать «..», «//», ведущий и хвостовой слэш. Прежде чем обращаться к
// диску, его обязательно пропускают через CleanPath. Это псевдоним string:
// доменное имя лишь помечает в сигнатурах, что аргумент ещё «сырой».
type VirtualPath = string

// CleanedPath — нормализованный абсолютный путь от корня раздачи: разделитель
// «/», без «..», «//» и хвостового слэша (корень остаётся «/»). Именно эта форма
// уходит в ответы протокола и служит ключом checksum-кэша. Псевдоним string.
// (Сама функция нормализации называется CleanPath — без «ed».)
type CleanedPath = string

// FileSize — размер файла в байтах (совпадает по смыслу с proto.FileSize, но
// объявлен локально, чтобы vfs не тянул протокол в чисто файловые сигнатуры).
type FileSize = uint64

// FileCount — число файлов (например, сколько обычных файлов в раздаче).
type FileCount = uint64

// UnixNanos — время в наносекундах от эпохи Unix. Используется как ЧАСТЬ КЛЮЧА
// checksum-кэша (mtime с наносекундной гранулярностью, CR-09) — в отличие от
// проводного DirEntry.Mtime, который остаётся в unix-СЕКУНДАХ.
type UnixNanos = uint64
