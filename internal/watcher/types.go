package watcher

// ─────────────────────────────────────────────────────────────────────────────
// types.go — словарь предметной области пакета watcher.
//
// Watcher превращает изменения файлов под корнем раздачи в события протокола
// EVENT_FS. Он оборачивает fsnotify (кроссплатформенно) и добавляет две вещи,
// которые эталон на C++ делал вручную (docs/tz/09-go-port.md §5.7):
//
//   1. РЕКУРСИВНОЕ слежение. fsnotify следит за одной директорией, не за
//      поддеревом. Watcher при старте подписывается на каждую поддиректорию, а
//      при создании новой — подписывается и на неё; при удалении/переименовании
//      поддерева — снимает подписки, чтобы их набор не рос без границ (§8 bug 15).
//
//   2. DEBOUNCE («схлопывание»). Одно сохранение файла редактором порождает
//      пачку событий (Create + несколько Write + Chmod…). Watcher откладывает
//      выдачу на debounce и шлёт ОДНО итоговое событие, если за это время не
//      пришло новых. Так клиент не тонет в дубликатах.
//
// ─── Две формы пути ──────────────────────────────────────────────────────────
//
// Как и в пакете vfs, путь существует в двух видах, и их нельзя путать:
//
//	RealPath      "/srv/share/sub/a.txt"  ← абсолютный путь на настоящей ФС
//	   │  vpathOf()
//	   ▼
//	VirtualPath   "/sub/a.txt"            ← путь от корня раздачи, для протокола
//
// fsnotify оперирует RealPath; наружу (в Event и клиенту) уходит только
// VirtualPath. Путь вне корня даёт пустой VirtualPath и событие не рождается.
// ─────────────────────────────────────────────────────────────────────────────

// RealPath — абсолютный путь на настоящей файловой системе, как его сообщает
// fsnotify. Псевдоним string.
type RealPath = string

// VirtualPath — путь от корня раздачи с разделителем «/» (та же форма, что и
// CleanedPath в пакете vfs). Именно он уходит в EVENT_FS. Псевдоним string.
type VirtualPath = string
